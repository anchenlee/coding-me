package cn.youhui.api.activity;

/**
 * 活动请求
 * @author lijun
 * @since 2013-12-14
 */
public class ActivityRequest {
	
	private String uid;
	
	/**
	 * 活动key
	 */
	private String activityKey;
	
	/**
	 * 活动唯一标识
	 */
	private String activityUniqueId;
	
	/**
	 * 活动获得集分宝
	 */
	private int jfbNum;
	
	/**
	 * 连续参加天数
	 */
	private int runningDays = 0;
	
	public ActivityRequest(String uid, String activityKey,
			String activityUniqueId, int jfbNum, int runningDays) {
		super();
		this.uid = uid;
		this.activityKey = activityKey;
		this.activityUniqueId = activityUniqueId;
		this.jfbNum = jfbNum;
		this.runningDays = runningDays;
	}
	
	public ActivityRequest(String uid, String activityKey,
			String activityUniqueId, int jfbNum) {
		super();
		this.uid = uid;
		this.activityKey = activityKey;
		this.activityUniqueId = activityUniqueId;
		this.jfbNum = jfbNum;
	}

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getActivityKey() {
		return activityKey;
	}

	public void setActivityKey(String activityKey) {
		this.activityKey = activityKey;
	}

	public String getActivityUniqueId() {
		return activityUniqueId;
	}

	public void setActivityUniqueId(String activityUniqueId) {
		this.activityUniqueId = activityUniqueId;
	}

	public int getJfbNum() {
		return jfbNum;
	}

	public void setJfbNum(int jfbNum) {
		this.jfbNum = jfbNum;
	}

	public int getRunningDays() {
		return runningDays;
	}

	public void setRunningDays(int runningDays) {
		this.runningDays = runningDays;
	}
}
