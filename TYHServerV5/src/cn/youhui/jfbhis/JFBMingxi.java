package cn.youhui.jfbhis;

/**
 * 集分宝详细历史记录
 * @author hufan
 * @since 2014-9-15
 */
public class JFBMingxi {

	private String type;
	private DayMingxi dayMingxi;
	private TongjiMingxi tongjiMingxi;
	
	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public DayMingxi getDayMingxi() {
		return dayMingxi;
	}

	public void setDayMingxi(DayMingxi dayMingxi) {
		this.dayMingxi = dayMingxi;
	}

	public TongjiMingxi getTongjiMingxi() {
		return tongjiMingxi;
	}

	public void setTongjiMingxi(TongjiMingxi tongjiMingxi) {
		this.tongjiMingxi = tongjiMingxi;
	}
	
}
