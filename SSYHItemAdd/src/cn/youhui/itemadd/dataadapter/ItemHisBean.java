package cn.youhui.itemadd.dataadapter;

public class ItemHisBean {

	public String itemId;
	public String type;
	public String tokId;
	public String tokName;
	public String getItemId() {
		return itemId;
	}
	public void setItemId(String itemId) {
		this.itemId = itemId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTokId() {
		return tokId;
	}
	public void setTokId(String tokId) {
		this.tokId = tokId;
	}
	public String getTokName() {
		return tokName;
	}
	public void setTokName(String tokName) {
		this.tokName = tokName;
	}
	
	
}
